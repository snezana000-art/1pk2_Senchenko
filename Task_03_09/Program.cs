namespace Task_03_09
{
    internal class Program {
//        Вклад в банке составляет x рублей.Ежегодно он увеличивается на p процентов, после чего дробная часть копеек
//     отбрасывается. Каждый год сумма вклада становится больше. Определите, через сколько лет вклад составит не
//     менее y рублей.
//     Примеры
//     входные данные
    //     100
     //10
     //200
     //выходные данные
     //8




        static void Main()
        {
            //Начальная сумма
            Console.Write("Введите начальную сумму вклада (x): ");
            double x = double.Parse(Console.ReadLine());

            // Процентная ставка
            Console.Write("Введите процентную ставку (p): ");
            double p = double.Parse(Console.ReadLine());

            // Целевая сумма
            Console.Write("Введите целевую сумму (y): ");
            double y = double.Parse(Console.ReadLine());

            int years = 0; // Счетчик лет

            // Цикл для расчета суммы вклада каждый год
            while (x < y)
            {
                x += x * p / 100; // Увеличение суммы на p процентов
                x = Math.Floor(x * 100) / 100; // Отбрасывание дробной части копеек
                years++; // Увеличение счетчика лет
            }

            //результат
            Console.WriteLine($"Вклад достигнет {y} рублей через {years} лет.");
        }
    }
}
    
